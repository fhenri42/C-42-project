#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(char const *s, int fd)
{
	int a;

	a = 0;
	if (s != NULL)
	{
		while (s[a])
		{
			write(fd, &s[a], 1);
			a++;
		}
	}
}
